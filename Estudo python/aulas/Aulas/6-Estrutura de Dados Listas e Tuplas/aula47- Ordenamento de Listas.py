
#ORDENANDO LISTAS, EXEMPLOS ABAIXOS

lista= ['Cláudio','José','Maria','Beltrano','João','Fulano','Ciclano',]
print(lista)


lista.reverse()  #Utilizado para inverter a Lista
print(lista)

lista.reverse()  #Utilizado para inverter a Lista
print(lista)

lista.sort()  #Utilizado para que a lista fique ordenada de forma ascendente (do menor para o maior)
print(lista)

lista.sort(reverse=True)  #Utilizado para que a lista fique ordenada de forma descendente (do maior para o menor)
print(lista)

