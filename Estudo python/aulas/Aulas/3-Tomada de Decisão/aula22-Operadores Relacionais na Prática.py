
numero1 = input("Digite um número:")
numero1 = int(numero1) #Gambiarra para transformar uma variavel do tipo INTEIRO

numero2 = input("Digite um segundo número:")
numero2 = int(numero2) #Gambiarra para transformar uma variavel do tipo INTEIRO

if (numero1==numero2): #Operador de igualdade
    print("O número %d é igual ao número %d."%(numero1,numero2))
if (numero1!=numero2): #Operador de diferença
    print("O número %d é diferente do número %d."%(numero1,numero2))
if (numero1<numero2): #Operador menor que
    print("O número %d é menor que o número %d."%(numero1,numero2))
if (numero1>numero2): #Operador maior que
    print("O número %d é maior do que o número %d."%(numero1,numero2))
if (numero1>=numero2): #Operador maior ou igual que
    print("O número %d é maior ou igual ao número %d."%(numero1,numero2))
if (numero1<=numero2): #Operador menor ou igual que
    print("O número %d é menor ou igual ao número %d."%(numero1,numero2))

