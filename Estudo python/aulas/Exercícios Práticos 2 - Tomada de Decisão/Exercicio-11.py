#11) Faça um algoritmo que verifica se um determinado valor é do tipo decimal.
variavel = float(input("Informe um valor: "))

print(variavel)
if (type(variavel)==float):
    print("O valor informado é um decimal.")
if (type(variavel)==int):
    print("O valor informado não é um decimal!!")
if (type(variavel)==str):
    print("O valor informado não é um decimal!!")




# O valor informado não é um decimal!!