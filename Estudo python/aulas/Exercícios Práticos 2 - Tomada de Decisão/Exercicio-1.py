
# Faça um algoritmo que leia um número e mostre se o mesmo é positivo, negativo ou zero.

num1 = int(input("Por favor informe um número:"))

if (num1==0):
    print("O número informado é 0.")
if (num1<0):
    print("O número informado é negativo.")
if (num1>0):
    print("O número informado é positivo.")

