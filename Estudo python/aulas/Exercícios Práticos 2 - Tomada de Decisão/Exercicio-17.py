"""
17) Faça um algoritmo que receba um número inteiro,
que represente um determinado mês do ano, e mostre o nome do mês correspondente. Por exemplo,
se for informado o número 2, o algoritmo deverá exibir: fevereiro.
O algoritmo também deve validar se a entrada está correta.
"""


mes=int(input("Digite um número para saber o mês:"))

if (mes==1):
    print("O número digitado foi 1 e o mês equivalente é Janeiro")
if (mes==2):
    print("O número digitado foi 2 e o mês equivalente é Fevereiro")
if (mes==3):
    print("O número digitado foi 3 e o mês equivalente é Março")
if (mes==4):
    print("O número digitado foi 4 e o mês equivalente é Abril")
if (mes==5):
    print("O número digitado foi 5 e o mês equivalente é Maio")
if (mes==6):
    print("O número digitado foi 6 e o mês equivalente é Junho")
if (mes==7):
    print("O número digitado foi 7 e o mês equivalente é Julho")
if (mes==8):
    print("O número digitado foi 8 e o mês equivalente é Agosto")
if (mes==9):
    print("O número digitado foi 9 e o mês equivalente é Setembro")
if (mes==10):
    print("O número digitado foi 10 e o mês equivalente é Outubro")
if (mes==11):
    print("O número digitado foi 11 e o mês equivalente é Novembro")
if (mes==12):
    print("O número digitado foi 12 e o mês equivalente é Dezembro")
else:
    print("O número digitado não é equivalente a um mês!!!")





